package com.sist.vo;

public class CertificateVO {
	private int no;
	private String caredate;
	private String prescription;
	private String doctor;
	private String dogname;
	private String hospitalname;
	private String dtype;
	private String dbirth;
	private String name;
	private String tel;
	private String vaccination_care;
	
	public String getVaccination_care() {
		return vaccination_care;
	}
	public void setVaccination_care(String vaccination_care) {
		this.vaccination_care = vaccination_care;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getCaredate() {
		return caredate;
	}
	public void setCaredate(String caredate) {
		this.caredate = caredate;
	}
	public String getPrescription() {
		return prescription;
	}
	public void setPrescription(String prescription) {
		this.prescription = prescription;
	}
	public String getDoctor() {
		return doctor;
	}
	public void setDoctor(String doctor) {
		this.doctor = doctor;
	}
	public String getDogname() {
		return dogname;
	}
	public void setDogname(String dogname) {
		this.dogname = dogname;
	}
	public String getHospitalname() {
		return hospitalname;
	}
	public void setHospitalname(String hospitalname) {
		this.hospitalname = hospitalname;
	}
	public String getDtype() {
		return dtype;
	}
	public void setDtype(String dtype) {
		this.dtype = dtype;
	}
	public String getDbirth() {
		return dbirth;
	}
	public void setDbirth(String dbirth) {
		this.dbirth = dbirth;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
}
